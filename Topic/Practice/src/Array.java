public class Array {

    public static void main(String[] args) {
        // Array Declaration and Initialization
        int[] arr = {10, 20, 30, 40, 50};

        // Print Array Elements
        System.out.println("Array Elements:");

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }

    }
}
